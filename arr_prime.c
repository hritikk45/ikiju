#include <stdio.h>
void main()
{
    int arr[100], i, n;
    printf("Enter the no of Elements:\n");
    scanf("%d", &arr[n]);
    for (i = 0; i < n; i++)
    {
        if (n % 2 == 0)
        {
            printf("This %d is not a prime no:", n);
        }
        else
        {
            printf("This %d is a prime no", n);
        }
    }
}
